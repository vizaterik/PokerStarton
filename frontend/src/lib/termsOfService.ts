export type LegalDoc = "terms" | "privacy" | "support";

export function legalDocMeta(doc: LegalDoc): { title: string; versionLabel: string | null; sections: TermsSection[] } {
  if (doc === "privacy") {
    return {
      title: "Политика конфиденциальности",
      versionLabel: `Редакция от ${TERMS_VERSION}`,
      sections: PRIVACY_SECTIONS,
    };
  }
  if (doc === "support") {
    return {
      title: "Контакты службы поддержки",
      versionLabel: null,
      sections: SUPPORT_SECTIONS,
    };
  }
  return {
    title: "Пользовательское соглашение",
    versionLabel: `Редакция от ${TERMS_VERSION}`,
    sections: TERMS_SECTIONS,
  };
}
